INSERT INTO app.term_days_off (term_id, date)
SELECT t.id, elem::date
FROM app.budgets b
JOIN app.terms t ON t.user_id = b.user_id
CROSS JOIN LATERAL jsonb_array_elements_text(b.holidays) AS elem;

